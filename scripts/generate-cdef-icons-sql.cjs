#!/usr/bin/env node
// Build M Sub-phase A — Generate INSERT VALUES SQL block from cdef-icons.json
//
// Reads:  scripts/build-m-seeds/cdef-icons.json (output of ingest-cdef-icons.cjs)
// Writes: scripts/build-m-seeds/cdef-icons.sql
//
// The output is a single INSERT block with ON CONFLICT DO NOTHING.
// Embedding column uses pgvector text format: '[0.1,0.2,...]'::halfvec(1536)

const fs = require('fs')
const path = require('path')

const IN_PATH = path.join(__dirname, 'build-m-seeds', 'cdef-icons.json')
const OUT_PATH = path.join(__dirname, 'build-m-seeds', 'cdef-icons.sql')

const rows = JSON.parse(fs.readFileSync(IN_PATH, 'utf8'))
console.log(`Loaded ${rows.length} rows from ${IN_PATH}`)

function sqlString(s) {
  if (s === null || s === undefined) return 'NULL'
  return `'${String(s).replace(/'/g, "''")}'`
}

function sqlJsonb(obj) {
  if (obj === null || obj === undefined) return 'NULL'
  return `${sqlString(JSON.stringify(obj))}::jsonb`
}

function sqlTextArray(items) {
  if (items === null || items === undefined) return 'NULL'
  if (!Array.isArray(items) || items.length === 0) return `'{}'::text[]`
  const escaped = items
    .map((s) => {
      const str = String(s)
      if (/[,"\s\\{}]/.test(str)) {
        return `"${str.replace(/\\/g, '\\\\').replace(/"/g, '\\"')}"`
      }
      return str
    })
    .join(',')
  return `'{${escaped}}'::text[]`
}

function sqlHalfvec(arr) {
  // pgvector accepts text format '[1.2,3.4,...]' which casts to halfvec(1536)
  if (!Array.isArray(arr) || arr.length !== 1536) {
    throw new Error(`Bad embedding length: ${arr?.length}`)
  }
  // Round to 6 decimal places to keep file size reasonable while preserving precision
  const compact = arr.map((n) => n.toFixed(6)).join(',')
  return `'[${compact}]'::halfvec(1536)`
}

const valueLines = []
for (const r of rows) {
  // NOTE: id is auto-generated by gen_random_uuid() default — we don't insert it
  // so we don't conflict with the existing 328 rows
  const cols = [
    sqlString(r.feature_key),
    sqlString(r.variant),
    sqlString(r.category),
    sqlString(r.size_512_url),
    sqlString(r.size_128_url),
    sqlString(r.size_32_url),
    sqlString(r.description),
    sqlString(r.generation_prompt),
    sqlJsonb(r.tags),
    sqlTextArray(r.vibe_compatibility),
    sqlString(r.display_name),
    sqlString(r.assigned_to),
    sqlString(r.status),
    sqlHalfvec(r.embedding),
  ]
  valueLines.push(`  (${cols.join(', ')})`)
}

const sql = `-- ============================================================
-- Build M Sub-phase A — visual_schedule Grids C/D/E/F seed (with embeddings)
-- ============================================================
-- 64 paper-craft icons across 4 grids:
--   Grid C — Daily Essentials       (8 subjects)
--   Grid D — Faith, Disability, Kindness (8 subjects)
--   Grid E — Morning & Evening Routine (12 subjects)
--   Grid F — Emotional Regulation & Social Skills (12 subjects)
--
-- Variant mapping: -1 → B (default), -2 → A
-- 24 subjects have both A+B variants, 16 subjects have B only.
-- Total rows: 24*2 + 16*1 = 64
--
-- Pipeline (one-shot, completed during Sub-phase A):
--   1. scripts/ingest-cdef-icons.cjs read each .jpg, called the
--      describe-vs-icon Edge Function (Sonnet vision via OpenRouter) for
--      a description + suggested_tags, called embed-text-admin (OpenAI
--      text-embedding-3-small) for the 1536-dim embedding, uploaded the
--      .jpg to Supabase Storage at platform-assets/visual-schedule/512/,
--      and wrote scripts/build-m-seeds/cdef-icons.json
--   2. scripts/generate-cdef-icons-sql.cjs converted that JSON to this
--      SQL VALUES block
--   3. The migration that includes this block is wrapped at
--      supabase/migrations/00000000100116_visual_schedule_cdef.sql
--
-- Storage URLs are deterministic and already populated in production
-- (uploaded by step 1). The migration is idempotent via ON CONFLICT
-- DO NOTHING on (feature_key, variant, category) — re-runs are safe.
-- ============================================================

INSERT INTO public.platform_assets (
  feature_key, variant, category,
  size_512_url, size_128_url, size_32_url,
  description, generation_prompt,
  tags, vibe_compatibility,
  display_name, assigned_to, status,
  embedding
) VALUES
${valueLines.join(',\n')}
ON CONFLICT (feature_key, variant, category) DO NOTHING;
`

fs.writeFileSync(OUT_PATH, sql)
const sizeKb = (fs.statSync(OUT_PATH).size / 1024).toFixed(1)
console.log(`Wrote ${OUT_PATH} (${sizeKb} KB, ${rows.length} value rows)`)
